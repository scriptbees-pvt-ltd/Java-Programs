package org.jsp.methods;

public class MethodWithNoArgumentsAndNoReturnValues 
{
	//Declaring method with no arguments and no return values;
	public static void run()
	{
		System.out.println("Method with no arguments and return values");
	}
	
	//Calling the run() from main();
	public static void main(String[] args) 
	{
		run(); //Method calling
	}
}
